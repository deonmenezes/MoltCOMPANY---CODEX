  teams_app_id text,
  teams_app_password text,
  whatsapp_phone_id text,
  whatsapp_access_token text,
  llm_api_key text,
  gateway_token text,
  region text default 'ap-south-1',
  bot_id text,
  companion_name text,
  companion_role text,
  companion_color text,
  companion_avatar text,
  character_files jsonb,
  created_at timestamptz default now(),
  last_health_check timestamptz
