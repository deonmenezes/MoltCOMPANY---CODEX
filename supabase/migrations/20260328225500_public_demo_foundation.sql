create extension if not exists pgcrypto;

create table if not exists public.users (
  id uuid primary key default gen_random_uuid(),
  email text,
  phone text,
  name text,
  google_id text,
  stripe_customer_id text,
  avatar_url text,
  bio text,
  created_at timestamptz default now()
);

