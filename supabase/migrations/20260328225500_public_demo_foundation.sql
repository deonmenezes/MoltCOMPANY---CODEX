alter table public.users add column if not exists bio text;
alter table public.users add column if not exists google_id text;
alter table public.users add column if not exists stripe_customer_id text;

create unique index if not exists users_email_key on public.users (email) where email is not null;
create unique index if not exists users_phone_key on public.users (phone) where phone is not null;
create unique index if not exists users_google_id_key on public.users (google_id) where google_id is not null;
create unique index if not exists users_stripe_customer_id_key on public.users (stripe_customer_id) where stripe_customer_id is not null;

